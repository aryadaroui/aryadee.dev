<p>
	When I first started learning how to code in C++ eight years ago, I used Eclipse as my main IDE
	along with a very colorful syntax highlighting scheme called <a
		href="http://www.eclipsecolorthemes.org/?view=theme&amp;id=24587">RainbowDrops</a
	>. I moved on from Eclipse, but there has always been a small hole in my heart every time I code
	with fewer colors. This theme, bubble-dark, is an attempt to fill that hole.
</p>
<p>
	bubble-dark is a VS Code color theme available on the <a
		href="https://marketplace.visualstudio.com/items?itemName=aryadaroui.bubble-dark&amp;utm_source=VSCode.pro&amp;utm_campaign=AhmadAwais"
		>marketplace</a
	>. I made it for myself, but it's gotten attention from others, so I figured it'd be nice to share
	it properly. It draws inspiration from the aforementioned RainbowDrops, and from the colors found
	from <a href="https://en.wikipedia.org/wiki/Thin-film_interference">thin-film interference</a> on bubbles.
</p>

<h2>/// Details</h2>
<p>bubble-dark aims to have <em>meaningful</em> syntax highlighting to the extent that VS Code's syntax scope tools will allow. Operators, keywords, identifiers, function definitions, function calls, etc. all have different colors that (subjectively) make sense, allowing you to get a better feeling for the structure of your code at a glance.</p>
<p>But be warned:  some may find it to be too much, since other themes typically have fewer colors in an effort for greater generalizability between languages. In contrast, bubble-dark was purpose-built for Python and C (because that's what I use the most). It'll work just fine with other languages, but there won't be the same level and usage of colors due to how VS code handles syntax scope.</p>


